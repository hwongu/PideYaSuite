    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (validarCampos()) {
                clienteEmpresa = new ClienteEmpresa();
                clienteEmpresa.setRazonSocial(txtRazonSocial.getText().trim().toUpperCase());
                clienteEmpresa.setRuc(txtRuc.getText().trim().toUpperCase());
                clienteEmpresa.setUsuario(new Usuario());
                clienteEmpresa.getUsuario().setCorreo(this.txtUsuario.getText());
                clienteEmpresa.getUsuario().setClave(txtClave.getText());
                clienteEmpresa.getUsuario().setTipoUsuario(TipoUsuario.CLIENTE_EMPRESA);
                if (this.txtCodigo.getText().trim().length() <= 0 || this.txtCodigo.getText().equalsIgnoreCase("")) {
                    //guardar
                    clienteEmpresa = clienteEmpresaService.agregarClienteEmpresa(clienteEmpresa);
                    if (clienteEmpresa != null) {
                        txtCodigo.setText(String.valueOf(clienteEmpresa.getIdCliente()));
                        txtCodigoUsuario.setText(String.valueOf(clienteEmpresa.getUsuario().getIdUsuario()));
                        enviarMensajeExito("Se registró el Cliente Empresa con ID: " + clienteEmpresa.getIdCliente());
                    }
                } else {
                    //actualizar
                    clienteEmpresa.setIdCliente(Integer.valueOf(txtCodigo.getText()));
                    clienteEmpresa.getUsuario().setIdUsuario(Integer.valueOf(txtCodigoUsuario.getText()));
                    clienteEmpresa = clienteEmpresaService.editarInformacionClienteEmpresa(clienteEmpresa);
                    if (clienteEmpresa != null) {
                        txtCodigo.setText(String.valueOf(clienteEmpresa.getIdCliente()));
                        enviarMensajeExito("Se actualizó el Cliente Empresa con ID: " + clienteEmpresa.getIdCliente());
                    }
                }
                inicializarTablaClienteEmpresa(null);
            }
        } catch (Exception ex) {
            enviarMensajeError("Ocurrio un error: " + ex.getMessage());
            Logger.getLogger(FrmCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

