    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            if (this.txtCodigo.getText().trim().length() <= 0 || this.txtCodigo.getText().equalsIgnoreCase("")) {
                enviarMensajeError("No existe Cliente Empresa para eliminar");
            } else {
                int opc = JOptionPane.showConfirmDialog(
                        this,
                        "¿Desea eliminar el Cliente seleccionado?",
                        "Confirmar eliminación",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE
                );
                if (opc != JOptionPane.YES_OPTION) {
                    return;
                }
                clienteEmpresa = new ClienteEmpresa();
                clienteEmpresa.setIdCliente(Integer.valueOf(txtCodigo.getText()));
                clienteEmpresa.setUsuario(new Usuario());
                clienteEmpresa.getUsuario().setIdUsuario(Integer.valueOf(txtCodigo.getText()));
                clienteEmpresaService.eliminarClienteEmpresa(clienteEmpresa);
                enviarMensajeExito("Se eliminó de manera el Cliente Empresa: " + this.txtRazonSocial.getText());
                limpiar();
            }
        } catch (Exception ex) {
            enviarMensajeError("Ocurrio un error: " + ex.getMessage());
            Logger.getLogger(FrmCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void limpiar() {
        configurarTxtRuc();
        configurarTxtUsuario();
        txtUsuario.setText("");
        txtRuc.setText("0000000000");
        txtCodigo.setText("");
        txtCodigoUsuario.setText("");
        txtClave.setText("");
        txtRazonSocial.setText("");
        txtRazonSocial.requestFocusInWindow();
        clienteEmpresa = new ClienteEmpresa();
        this.inicializarTablaClienteEmpresa(null);
    }

    private void inicializarTablaClienteEmpresa(List<ClienteEmpresa> clientes) {
        // 1) Creo el modelo con 4 columnas: OBJ (oculta), ID, Razón Social, RUC
        DefaultTableModel model = new DefaultTableModel(
                new String[]{"OBJ", "ID", "Razón Social", "RUC"},
                0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                if (columnIndex == 0) {
                    return ClienteEmpresa.class; // guardo el objeto completo
                }
                return super.getColumnClass(columnIndex);
            }
        };

        // 2) Relleno filas
        if (clientes != null) {
            for (ClienteEmpresa ce : clientes) {
                model.addRow(new Object[]{
                    ce, // 0) objeto
                    ce.getIdCliente(), // 1) ID
                    ce.getRazonSocial().toUpperCase(), // 2) Razón Social
                    ce.getRuc() // 3) RUC
                });
            }
        }

        // 3) Asigno el modelo al JTable
        jtFiltro.setModel(model);

        // 4) Ajusto anchos de columna
        // Ocultar columna 0 (OBJ)
        jtFiltro.getColumnModel().getColumn(0).setMinWidth(0);
        jtFiltro.getColumnModel().getColumn(0).setMaxWidth(0);
        jtFiltro.getColumnModel().getColumn(0).setWidth(0);

        // ID fijo en 50px
        jtFiltro.getColumnModel().getColumn(1).setMinWidth(50);
        jtFiltro.getColumnModel().getColumn(1).setMaxWidth(50);

        // Dejo que las demás se ajusten
        jtFiltro.setAutoResizeMode(JTable.AUTO_RESIZE_SUBSEQUENT_COLUMNS);

        // 5) Listener para doble clic: vuelca datos al formulario
        jtFiltro.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent e) {
                if (e.getClickCount() == 2 && !e.isConsumed()) {
                    e.consume();
                    int row = jtFiltro.getSelectedRow();
                    if (row >= 0) {
                        // Obtenemos el objeto Categoria de la columna oculta
                        ClienteEmpresa seleccion = (ClienteEmpresa) jtFiltro.getModel().getValueAt(row, 0);
                        // Rellenar los text fields
                        txtCodigo.setText(String.valueOf(seleccion.getIdCliente()));
                        txtRazonSocial.setText(seleccion.getRazonSocial());
                        txtRuc.setText(seleccion.getRuc());
                        txtCodigoUsuario.setText(String.valueOf(seleccion.getUsuario().getIdUsuario()));
                        txtUsuario.setText(seleccion.getUsuario().getCorreo());
                        txtClave.setText(seleccion.getUsuario().getClave());
                        //txtTipoUsuario.setText(seleccion.getUsuario().getTipoUsuario().getDescripcion());
                        jtbPrincipal.setSelectedIndex(1);
                    }
                }
            }
        });
        jtFiltro.getTableHeader().setReorderingAllowed(false);
    }

    public void configurarTxtRuc() {
        try {
            MaskFormatter rucMask = new MaskFormatter("###########");
            rucMask.setPlaceholderCharacter('0');
            rucMask.setAllowsInvalid(false);
            txtRuc.setFormatterFactory(
                    new DefaultFormatterFactory(rucMask)
            );
            txtRuc.setColumns(11);

            txtRuc.setInputVerifier(new InputVerifier() {
                @Override
                public boolean verify(JComponent input) {
                    String text = ((JFormattedTextField) input).getText();
                    if (!text.matches("(10|20)\\d{9}")) {
                        enviarMensajeError("RUC inválido: debe tener 11 dígitos y empezar con 10 o 20.");
                        return false;
                    }
                    return true;
                }
            });
        } catch (ParseException ex) {
            Logger.getLogger(FrmCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void configurarTxtUsuario() {
        // Regex básico para correos comunes
        Pattern emailPattern = Pattern.compile(
                "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$"
        );

        txtUsuario.setInputVerifier(new InputVerifier() {
            @Override
            public boolean verify(JComponent input) {
                String text = ((JFormattedTextField) input).getText().trim();
                if (!emailPattern.matcher(text).matches()) {
                    enviarMensajeError("Formato de correo inválido.\nDebe ser algo@dominio.com");
                    return false;  // no permite perder el foco hasta corregir
                }
                return true;
            }
        });
    }

    private boolean validarCampos() {
        if (txtRazonSocial.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "La Razón Social es obligatoria.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE
            );
            txtRazonSocial.requestFocusInWindow();
            return false;
        }
        if (txtRuc.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "El RUC es obligatorio.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE
            );
            txtRuc.requestFocusInWindow();
            return false;
        }

        // Datos de Usuario
        if (txtUsuario.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "El Usuario es obligatorio.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE
            );
            txtUsuario.requestFocusInWindow();
            return false;
        }
        if (txtClave.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "La Clave es obligatoria.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE
            );
            txtClave.requestFocusInWindow();
            return false;
        }
        return true;
    }

